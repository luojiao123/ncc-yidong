package com.entity.help;

public class NodeList {
 
	private Object title;
	private Object time;
	private Object flag;
	private Object id;
	private Object N;
	private Object S;
	private Object name;
	private Object code;
 
	public Object getTitle() {
		return title;
	}
	public void setTitle(Object title) {
		this.title = title;
	}
	public Object getTime() {
		return time;
	}
	public void setTime(Object time) {
		this.time = time;
	}
	public Object getFlag() {
		return flag;
	}
	public void setFlag(Object flag) {
		this.flag = flag;
	}
	public Object getId() {
		return id;
	}
	public void setId(Object id) {
		this.id = id;
	}
	public Object getN() {
		return N;
	}
	public void setN(Object n) {
		N = n;
	}
	public Object getS() {
		return S;
	}
	public void setS(Object s) {
		S = s;
	}
	public Object getName() {
		return name;
	}
	public void setName(Object name) {
		this.name = name;
	}
	public Object getCode() {
		return code;
	}
	public void setCode(Object code) {
		this.code = code;
	}
	
}
