package com.entity.help;

public class StoreMessage {

	private Object area;           //地区
	private Object city;           //城市
	private Object code;           //门店编号
	private Object storename;      //门店名字
	private Object clientele;      //品牌
	private Object status;         //设备网络状态
	private Object id;             //设备id
	private Object count;          //设备播放状态
	private Object downsucesslist; //最近成功下载列表时间
	private Object newestlist;     //最新下发列表时间
	private Object newestlistflag; //最新下发列表是否下载

	public Object getArea() {
		return area;
	}
	public void setArea(Object area) {
		this.area = area;
	}
	public Object getCity() {
		return city;
	}
	public void setCity(Object city) {
		this.city = city;
	}
	public Object getCode() {
		return code;
	}
	public void setCode(Object code) {
		this.code = code;
	}
	public Object getStorename() {
		return storename;
	}
	public void setStorename(Object storename) {
		this.storename = storename;
	}
	public Object getClientele() {
		return clientele;
	}
	public void setClientele(Object clientele) {
		this.clientele = clientele;
	}
	public Object getStatus() {
		return status;
	}
	public void setStatus(Object status) {
		this.status = status;
	}
	public Object getId() {
		return id;
	}
	public void setId(Object id) {
		this.id = id;
	}
	public Object getCount() {
		return count;
	}
	public void setCount(Object count) {
		this.count = count;
	}
	public Object getDownsucesslist() {
		return downsucesslist;
	}
	public void setDownsucesslist(Object downsucesslist) {
		this.downsucesslist = downsucesslist;
	}
	public Object getNewestlist() {
		return newestlist;
	}
	public void setNewestlist(Object newestlist) {
		this.newestlist = newestlist;
	}
	public Object getNewestlistflag() {
		return newestlistflag;
	}
	public void setNewestlistflag(Object newestlistflag) {
		this.newestlistflag = newestlistflag;
	}
	
	
	 
}
